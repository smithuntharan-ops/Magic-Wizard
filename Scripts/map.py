def draw_grids():
   for i in range(60):
      pygame.draw.line(Background_img, (255, 255, 255), (i*32, 0), (i*32, 960))

   for i in range(40):
      pygame.draw.line(Background_img, (255, 255, 255), (0, i*32), (1920, i*32))

def load_images():
   path = 'Assets\\Tileset'
   for imgid, name in enumerate(listdir(path)):
      img = pygame.image.load(path + '\\' + name)
      img = pygame.transform.scale(img, (32, 32))
      name = name[:-4]
      Tiles_List[name] = [img, img.get_rect(), imgid]

def fill_sidepanel():
   load_images()
   ctr = 0
   lst = [820, 150]
   for img in Tiles_List:
      ctr += 1
      rec = Tiles_List[img][1]
      img = Tiles_List[img][0]
      screen.blit(img, (lst[0], lst[1]))
      rec.x, rec.y = lst[0], lst[1]
      lst[0] += 150
      if ctr == 2:
         ctr = 0
         lst[0] = 820 
         lst[1] += 50

def update_bg():
   global shiftx, shifty, tile_pos
   K = pygame.key.get_pressed()
   
   if K[pygame.K_RIGHT] and Background_img_rect.x > -1160: 
      Background_img_rect.x -= 8
      shiftx -= 8
   if K[pygame.K_LEFT] and Background_img_rect.x < 0: 
      Background_img_rect.x += 8
      shiftx += 8
   if K[pygame.K_DOWN] and Background_img_rect.y > -320: 
      Background_img_rect.y -= 8 
      shifty -= 8
   if K[pygame.K_UP] and Background_img_rect.y < 0: 
      Background_img_rect.y += 8
      shifty += 8

   tile_pos = [(mousex - shiftx)//32, (mousey - shifty)//32]
   
def collision_mouse(data):
   global current_tile_selected
   
   M = pygame.mouse.get_pressed()
   for name in data:
      rec = Tiles_List[name][1]
      if rec.x <= mousex <= rec.x + 32 and rec.y <= mousey <= rec.y + 32:
         if M[0]:
            current_tile_selected = name
      
      # Outline
      if name == current_tile_selected:
            pygame.draw.rect(screen, (0, 0, 0), rec, 2)

def collision_button(data):
   global current_level, trigger, tiles_in_map

   M = pygame.mouse.get_pressed()
   for name in data:
      rec = Buttons_List[name][1]
      if rec.x <= mousex <= rec.x + 32 and rec.y <= mousey <= rec.y + 32:
         if M[0] and name == 'next' and trigger:
            current_level += 1
            trigger = False
            
            # Load respective world
            load_world(current_level)

         if M[0] and name == 'prev' and current_level > 1 and trigger:
            current_level -= 1
            trigger = False
            
            # Load respective world
            load_world(current_level)
         
         if not M[0]:
            trigger = True 
                 
def draw_buttons():
   for button in Buttons_List:
      img = Buttons_List[button][0]
      rec = Buttons_List[button][1]
      screen.blit(img, rec)

def place_update_tiles():
   global current_tile_selected, tiles_in_map

   path = 'Assets\\Tileset\\'
   M = pygame.mouse.get_pressed()
   if mousex < 760:
      if M[0]:

         # Visual
         img = pygame.image.load(path + current_tile_selected + '.png')
         rec = img.get_rect()
         rec.x, rec.y = (tile_pos[0] - shiftx) * 32, (tile_pos[1] - shifty) * 32
         tiles_in_map.append((img, rec, tile_pos))

         # Data
         temp_world[tile_pos[1]][tile_pos[0]] = Tiles_List[current_tile_selected][2]
      
      if M[2]:
         for data in tiles_in_map:

               # Visual
            rec = data[1]
            if rec.x <= mousex <= rec.x + 32 and rec.y <= mousey <= rec.y + 32:
               tiles_in_map.remove(data)

               # Data
               temp_world[tile_pos[1]][tile_pos[0]] = ' '
               break


   for img, rec, tile_pos_ in tiles_in_map:
      screen.blit(img, rec)
      rec.x, rec.y = (tile_pos_[0] * 32) + shiftx, (tile_pos_[1] * 32) + shifty

